    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(campoId.getText().equals("")){
        JOptionPane.showMessageDialog(null, "Primero digiteel ID de la escuela",
                "error", JOptionPane.ERROR_MESSAGE);
        return;
        }
        
        String sql ="SELECT nombre, fecha, cantidadalumnos, area,"
                + " email, telefono, direccion FROM escuelas\n" +
               "WHERE id="+Integer.parseInt(campoId.getText())+";";
        
        try{
         String d[] = new String[7];
         ResultSet r = sentencia.executeQuery(sql);
         r.next();
         
         
         d[0]= r.getString("nombre");
         d[1]= r.getString("fecha");
         d[2]= r.getString("cantidadalumnos");
         d[3]= r.getString("area");
         d[4]= r.getString("email");
         d[5]= r.getString("telefono");
         d[6]= r.getString("direccion");
         
         campoNombre.setText(d[0]);
        String fecha[] = d[1].split("-");
        SpinnerNumberModel modeloDia = new SpinnerNumberModel();
        modeloDia.setMaximum(31);
        modeloDia.setMinimum(1);
        modeloDia.setValue(Integer.parseInt(fecha[2]));
        spidia.setModel(modeloDia);
        spidia.setEditor(new JSpinner.DefaultEditor(spidia));
        
        SpinnerNumberModel modeloMes = new SpinnerNumberModel();
        modeloMes.setMaximum(12);
        modeloMes.setMinimum(1);
        modeloMes.setValue(Integer.parseInt(fecha[1]));
        spiMes.setModel(modeloMes);
        spiMes.setEditor(new JSpinner.DefaultEditor(spiMes));

        SpinnerNumberModel modeloAnio = new SpinnerNumberModel();
        modeloAnio.setMaximum(3000);
        modeloAnio.setMinimum(1910);
        modeloAnio.setValue(Integer.parseInt(fecha[0]));
        spiAnio.setModel(modeloAnio);
        spiAnio.setEditor(new JSpinner.DefaultEditor(spiAnio));
        
        campoTotalAlumnos.setText(d[2]);
        campoArea.setText(d[3]);
        campoEmail.setText(d[4]);
        campoTelefono.setText(d[5]);
        campoDireccion.setText(d[6]);
        
        
        }catch(SQLException ex){
        
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

