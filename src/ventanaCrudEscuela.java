    public void activos(boolean b1,boolean b2,boolean b3,boolean b4, boolean campoID){
        btnAgregar.setEnabled(b1);
        btnBuscar.setEnabled(b2);
        btnEliminar.setEnabled(b3);
        btnActualizar.setEnabled(b4);
        this.campoId.setEditable(campoID);
        
        
        String fecha[];
        Date daano = new Date();
        SimpleDateFormat fe = new SimpleDateFormat("dd-MM-YYYY");
        String auxi = String.valueOf(fe.format(daano));
        fecha = auxi.split("-");
        
        SpinnerNumberModel modeloDia = new SpinnerNumberModel();
        modeloDia.setMaximum(31);
        modeloDia.setMinimum(1);
        modeloDia.setValue(Integer.parseInt(fecha[0]));
        spidia.setModel(modeloDia);
        spidia.setEditor(new JSpinner.DefaultEditor(spidia));
        
        SpinnerNumberModel modeloMes = new SpinnerNumberModel();
        modeloMes.setMaximum(12);
        modeloMes.setMinimum(1);
        modeloMes.setValue(Integer.parseInt(fecha[1]));
        spiMes.setModel(modeloMes);
        spiMes.setEditor(new JSpinner.DefaultEditor(spiMes));

        SpinnerNumberModel modeloAnio = new SpinnerNumberModel();
        modeloAnio.setMaximum(3000);
        modeloAnio.setMinimum(1910);
        modeloAnio.setValue(Integer.parseInt(fecha[2]));
        spiAnio.setModel(modeloAnio);
        spiAnio.setEditor(new JSpinner.DefaultEditor(spiAnio));
        
        
        
    }
